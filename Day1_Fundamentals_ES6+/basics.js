// Variables
var name = "John Doe"; // var is function-scoped
let age = 25; // let is block-scoped
const country = "USA"; // const is block-scoped and cannot be reassigned

// Data Types
let isStudent = true; // boolean
let score = null; // null
let city; // undefined
let subjects = ["Math", "Science", "English"]; // array
let person = {
  // object
  firstName: "Jane",
  lastName: "Doe",
};

// Operators
let a = 5;
let b = 10;
let sum = a + b; // addition
let product = a * b; // multiplication
let isEqual = a === b; // comparison
let isAdult = age >= 18; // comparison
